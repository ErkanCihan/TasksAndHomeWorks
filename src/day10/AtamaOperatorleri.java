package Day10;

public class AtamaOperatorleri {
    public static void main(String[] args) {

        //Bir değişkenin değerini artırıp tekrar aynı değişken içerisinde saklamak:
        int t=1;
        t+=1;
        t+=3;

        //Bir değişkenin değerini azaltıp tekrar aynı değişken içerisinde saklamak:
        int k=7;
        k-=2;

        //Bir değişkenin değerini bir sayı ile çarpıp tekrar aynı değişken içerisinde saklamak:
        int m=8;
        m*=2;


    }
}
